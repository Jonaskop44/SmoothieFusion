import axios from "axios";

export class Helper {
  constructor() {}

  async getAllRecipes() {
    return axios
      .get("/recipes/")
      .then((response) => {
        if (response.status !== 200) return { data: null, status: false };

        const data = response.data;
        return { data: data, status: true };
      })
      .catch(() => {
        return { data: null, status: false };
      });
  }

  async getRecipeById(id: number) {
    return axios
      .get(`/recipes/${id}/`)
      .then((response) => {
        if (response.status !== 200) return { data: null, status: false };

        const data = response.data;
        return { data: data, status: true };
      })
      .catch(() => {
        return { data: null, status: false };
      });
  }
}
